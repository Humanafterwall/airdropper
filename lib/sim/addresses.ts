/** Pick a random wallet using crypto.getRandomValues */
export function pickRandomWallet(): `0x${string}` {
  const buf = new Uint32Array(1);
  crypto.getRandomValues(buf);
  return FAKE_WALLETS[buf[0] % FAKE_WALLETS.length];
}

/** Pick random entry count 1-3 */
export function pickRandomEntries(): number {
  const buf = new Uint32Array(1);
  crypto.getRandomValues(buf);
  return (buf[0] % 3) + 1;
}
